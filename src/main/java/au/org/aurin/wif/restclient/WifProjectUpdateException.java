package au.org.aurin.wif.restclient;

/**
 * The Class WifProjectUpdateException.
 */
public class WifProjectUpdateException extends Exception {
  
  /**
   * Instantiates a new wif project update exception.
   */
  public WifProjectUpdateException() {
    super();
  }
  
  /**
   * Instantiates a new wif project update exception.
   *
   * @param message the message
   */
  public WifProjectUpdateException(String message) {
    super(message);
  }
  
  /**
   * Instantiates a new wif project update exception.
   *
   * @param cause the cause
   */
  public WifProjectUpdateException(Throwable cause) {
    super(cause);
  }
  
  /**
   * Instantiates a new wif project update exception.
   *
   * @param message the message
   * @param cause the cause
   */
  public WifProjectUpdateException(String message, Throwable cause) {
    super(message, cause);
  }

}
